import { useCallback, useState } from "react";

type Users = {
  id: number,
  name: string,
  username: string,
  email: string,
  address: {
    street: string,
    suite: string,
    city: string,
    zipcode: string,
    geo: {
      lat: string,
      lng: string,
    },
  },
  phone: string,
  website: string,
  company: {
    name: string,
    catchPhrase: string,
    bs: string,
  },
};

export const useAllUsers = () => {
  const [users, setUsers] = useState([]);

  const fetchData = useCallback(async () => {
    try {
      const res = await fetch("https://jsonplaceholder.typicode.com/users");
      const data = await res.json();
      setUsers(data);
    } catch (error) {
      alert(error);
    }
  }, []);
  return { users, fetchData };
};
